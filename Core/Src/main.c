void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)       // this is isr for both ir interrupt and ZCD interrupt
{
  /* Prevent unused argument(s) compilation warning */
  UNUSED(GPIO_Pin);
	if(GPIO_Pin == GPIO_PIN_0)            // For IR pulse count
	{
		Count++;	
	}
  if(GPIO_Pin == GPIO_PIN_1)           // Means Interrupt caused by ZCD 
	{
		zero_cross = true;                 // Set the zero_cross flag
		HAL_GPIO_WritePin(GPIOB,GPIO_PIN_14,GPIO_PIN_RESET);     // Reset the TRIAC trigger pin
	}
}


void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)  // This is same callback function for TIM2 & TIM4
{
  /* Prevent unused argument(s) compilation warning */
  UNUSED(htim);
	if(htim->Instance==TIM2)       // This callback generated every 1 Sec
	{
		IrGetSpeed = Count/3;  // For three blade fan IR gives 3 times pulse in each revolution (revolution per second)
		Count = 0;           // Reseting the Count value
	
	}
	
	if(htim->Instance==TIM4)     // This callback generated every 10us
	{
		if(zero_cross == true)
		{
			PhaseShieft = ChopTime;
			if(PhaseShieft==0)
			{
				HAL_GPIO_WritePin(GPIOB,GPIO_PIN_14,GPIO_PIN_SET);
				zero_cross = false;
			}
			else{
				PhaseShieft--;
			}
		
		}
	
	}	

}
